function R=Quaternion_to_DCM(q)
% This function converts the Quaternion to Direction Cosine matrix
R=zeros(3,3);
% q(4) is the scalar part of the quaternion
R(:,1)=[q(1)^2-q(2)^2-q(3)^2+q(4)^2;...
    2*(q(1)*q(2)-q(3)*q(4));...
    2*(q(1)*q(3)+q(2)*q(4))];
R(:,2)=[2*(q(1)*q(2)+q(3)*q(4));...
    -q(1)^2+q(2)^2-q(3)^2+q(4)^2;...
    2*(q(2)*q(3)-q(1)*q(4))];
R(:,3)=[2*(q(1)*q(3)-q(2)*q(4));...
    2*(q(2)*q(3)+q(1)*q(4));...
    -q(1)^2-q(2)^2+q(3)^2+q(4)^2];
end
